            <h4 class="position-relative d-inline-block text-primary text-uppercase">Diabetes Tipe 1</h4>
            <p class="mb-4">Diabetes tipe 1, juga dikenal sebagai diabetes mellitus tipe 1, adalah bentuk diabetes autoimun di mana sistem kekebalan tubuh menyerang dan merusak sel-sel yang memproduksi insulin di pankreas. Kondisi ini sering muncul pada usia muda, dan penderita diabetes tipe 1 harus menggantikan insulin secara rutin untuk mengatur gula darah mereka.</p>

            <h4 class="position-relative d-inline-block text-primary text-uppercase">Diabetes Tipe 2</h4>
            <p class="mb-4">Diabetes tipe 2 adalah yang paling umum. Penderita diabetes tipe 2 masih memproduksi insulin, tetapi tubuh mereka tidak meresponsnya dengan baik. Faktor risiko termasuk obesitas, riwayat keluarga, dan gaya hidup yang tidak sehat. Pengaturan gula darah dapat dilakukan dengan diet, olahraga, obat-obatan, dan kadang-kadang insulin.</p>

