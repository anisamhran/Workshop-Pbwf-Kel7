            <h5 class="position-relative d-inline-block text-primary text-uppercase">Apa Itu Diabetes?</h5>

            <p class="mb-4">Diabetes mellitus, yang sering disebut diabetes, adalah kelompok penyakit yang memengaruhi cara tubuh mengatur gula darah. Gula darah adalah sumber utama energi tubuh, dan pengaturannya sangat penting. Diabetes terjadi ketika tubuh tidak dapat memproses gula darah dengan baik, menyebabkan kadar gula darah tinggi.</p>
