            <p class="mb-4">Diabetes gestasional adalah kondisi yang terjadi selama kehamilan. Hormon kehamilan dapat membuat tubuh lebih resisten terhadap insulin, yang dapat menyebabkan kadar gula darah meningkat. Diabetes gestasional umumnya menghilang setelah kelahiran bayi, tetapi dapat meningkatkan risiko terkena diabetes tipe 2 di masa depan.</p>
